#include <stdio.h> // Incluye la biblioteca estándar de entrada y salida

int main() { // Función principal
    double x = 3.14159; // Declara una variable double 'x' y le asigna un valor
    double y = 2.718281; // Declara una variable double 'y' y le asigna un valor
    double suma = x + y; // Declara una variable 'suma' y almacena la suma de 'x' y 'y'
    printf("La suma de %.5f y %.6f es %.6f\n", x, y, suma); // Imprime el resultado en pantalla
    return 0; // Indica que el programa terminó correctamente
}
